public class ModBlockLootTables extends BlockLootSubProvider {
    public ModBlockLootTables() {
        super(Set.of(), FeatureFlags.REGISTRY.allFlags());
    }

    @Override
    protected void generate() {
        this.dropSelf(ModBlocks.ENDER_PEARL_BLOCK.get());

        LootItemCondition.Builder lootitemcondition$builder = LootItemBlockStatePropertyCondition
                .hasBlockStateProperties(ModBlocks.SPECTRE_CROP.get())
                .setProperties(StatePropertiesPredicate.Builder.properties().hasProperty(SpectreCropBlock.AGE, 7));

        this.add(ModBlocks.SPECTRE_CROP.get(), createCropDrops(ModBlocks.SPECTRE_CROP.get(), ModItems.SPECTRE_WHEAT.get(),
                ModItems.SPECTRE_SEEDS.get(), lootitemcondition$builder));
    }

    @Override
    protected Iterable<Block> getKnownBlocks() {
        return ModBlocks.BLOCKS.getEntries().stream().map(RegistryObject::get)::iterator;
    }
}
